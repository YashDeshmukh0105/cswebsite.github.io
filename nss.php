<p>National Service Scheme (NSS) is a community service oriented unit of Loyola Academy started working from 1992. This unit consists of a chairman that is the principal of the college, any one lecturer in college act as programme officer, at present Dr.Gadde.Ratna Vani, HOD Indian Culture acting as PO of the unit. There are 105 student volunteers from different departments of Under Graduate Courses are serving in the unit. It is a registered body under Osmania University Zone, functioning on the guidelines of union ministry of Human Resource Development and the ministry of Youth affairs and Sports. The NSS unit observes and organizes various programmes on all International and National Importance days like Yoga day, World Human Rights Day, National Voters day, Environment day, AIDS, Women’s day, so on.</p>
<p>The unit take up various local, state and central government’s social programs like Clean & Green ‘Haritha Haaram’- plantation of trees, Voters Awareness and enrollment, Swatchh Bharath campaign, Blood Donation Camps and so on.NSS organizes various awareness Rallies against ill-effects of Drugs, Alcohol, AIDS, Suicides tendencies among youngsters etc. The unit also works by adopting slums and few local communities to bring awareness on sanitary issues. Students of NSS assist government agencies by take part in various surveys they conduct and immerse with rural India.</p>
